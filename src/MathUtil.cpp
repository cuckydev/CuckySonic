int16_t GetSin(uint8_t angle) { return sineTable[angle]; }
int16_t GetCos(uint8_t angle) { return sineTable[angle + 0x40]; }

uint8_t GetAtan(int16_t x, int16_t y)
{
	//If x and y is 0, return 90 degrees, then get our absolute x and y
	if (x == 0 && y == 0)
		return 0x40;
	
	uint16_t absX = mabs(x);
	uint16_t absY = mabs(y);
	
	//Get our absolute angle
	uint8_t angle;
	if (absY < absX)
		angle = atanTable[(absY <<= 8) /= absX];
	else
		angle = 0x40 - atanTable[(absX <<= 8) /= absY];
	
	//Invertion if negative
	if (x < 0)
		angle = -angle + 0x80;
	if (y < 0)
		angle = -angle + 0x100;
	return angle;
}

uint32_t RandomNumber()
{
	struct M68KREG
	{
		union
		{
			struct
			{
				#if ENDIAN == BIG
					//Big endian - high word first, low word second
					uint16_t high;
					uint16_t low;
				#else
					//Little endian - low word first, high word second
					uint16_t low;
					uint16_t high;
				#endif
			} w;
			uint32_t l = 0x00000000;
		};
	} static seed;
	
	//Re-seed if 0
	if (seed.l == 0)
		seed.l = 0x2A6D365A;
	
	//Scramble our current seed
	M68KREG retSeed = seed;			//move.l	d1,d0
	seed.l <<= 2;					//asl.l		#2,d1
	seed.l += retSeed.l;			//add.l		d0,d1
	seed.l <<= 3;					//asl.l		#3,d1
	seed.l += retSeed.l;			//add.l		d0,d1
	retSeed.w.low = seed.w.low;		//move.w	d1,d0
	
	//switch to seed.w.high because of a swap
	retSeed.w.low += seed.w.high;	//add.w		d1,d0
	seed.w.high = retSeed.w.low;	//move.w	d0,d1
	
	return retSeed.l;
}