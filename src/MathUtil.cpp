void GetSine(uint8_t angle, int16_t *sin, int16_t *cos)
{
	if (sin != nullptr)
		*sin = sineTable[angle];
	if (cos != nullptr)
		*cos = sineTable[angle + 0x40];
}

uint8_t GetAtan(int16_t x, int16_t y)
{
	if (x == 0 && y == 0)
		return 0x40;

	uint8_t angle = mabs(x) > mabs(y) ? AtanData[(mabs(y) * 0x100) / mabs(x)] : 0x40 - AtanData[(mabs(x) * 0x100) / mabs(y)];

	if (x < 0)
		angle = -angle + 0x80;
	if (y < 0)
		angle = -angle + 0x100;
	return angle;
}
